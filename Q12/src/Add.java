public class Add {
    Add(){
        System.out.println("\n\n1. Consrutor Overloading!!");
    }
    Add(int a, int b){
        System.out.println("The sum of "+a+" and "+b+" is :: "+(a+b));
    }
    Add(double a, double b){
        System.out.println("The sum of "+a+" and "+b+" is :: "+(a+b));
    }
}
